#include <iostream>
#include <cmath>

double add(int num1, int num2)
{
    return num1 + num2;
}

double multiply(int num1, int num2)
{
    return num1 * num2;
}

double subtract(int num1, int num2)
{
    return num1 - num2;
}

double divide(int num1, int num2)
{
    return num1 / num2;
}
double pwr(int num1, int num2)
{
    return pow(num1, num2);
 }